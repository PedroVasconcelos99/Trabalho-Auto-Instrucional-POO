package enemies;

public class Inimigos {
	protected int vida;
	protected int atk;
	protected int def;
	
	public Inimigos(int vida, int atk,int def) {
		
	}

	public int getVida() {
		return vida;
	}

	public void setVida(int vida) {
		this.vida = vida;
	}

	public int getAtk() {
		return atk;
	}

	public void setAtk(int atk) {
		this.atk = atk;
	}

	public int getDef() {
		return def;
	}

	public void setDef(int def) {
		this.def = def;
	}
	
	
	
	
}
